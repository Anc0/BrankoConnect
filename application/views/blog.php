            <h1 class="blog-header">
                What’s Wrong with Network Marketing
            </h1>
            <p class="blog-subtitle">Written by BRANKO KRAŠOVEC</p>
            <img class="blog-image-center"
                 src="http://elitemarketingpro.com/blog/wp-content/uploads/2017/06/82955_BlogBanner_051817_TW-2.png"/>
            <p class="blog-text">This is a controversial topic.</p>
            <p class="blog-text">Certain leaders don’t want to bring attention to the things that are blatantly wrong
                with the network
                marketing industry…but from my perspective, these issues MUST be addressed and corrected.</p>
            <p class="blog-text">So I’m just going to spell it out.</p>
            <p class="blog-text">And, being frank, I have a different take on the industry than the “orthodox,”
                old-school leaders.</p>
            <p class="blog-text">Now, I’m not saying that I’m right and everyone else is wrong (…you’ll have to decide
                that for yourself and
                what works best for you).</p>
            <p class="blog-text">But, just this once, I’m going to tell you exactly what I believe is wrong and what we
                can do about it.</p>
            <h1 class="blog-header">What’s WRONG with network marketing today</h1>
            <p class="blog-text">To kick things off…</p>
            <p class="blog-text">If you’re a top earner and you’re not a fan of somebody like me offering training to
                your people then this
                first item definitely applies to you, because it’s the primary reason networkers seek 3rd party
                training…</p>
            <img class="blog-image-center"
                 src="http://elitemarketingpro.com/blog/wp-content/uploads/2017/06/86520_BlogBanner1_060217_1.png"/>
            <p class="blog-text">The lack of skills-based training is the first thing that’s wrong with network
                marketing.</p>
            <p class="blog-text">That and the fact that the majority of company events are mostly hype.</p>
            <p class="blog-text">Not all, of course, but a majority of company events are just full of hype and have
                very little skills-based
                training.</p>
            <p class="blog-text">This is a huge problem because there are a lot of false expectations when people are
                first brought into the
                industry, and all those false expectations make it seem like it’s going to be easier than it really
                is.</p>
            <p class="blog-text">On top of that, we don’t properly train people.</p>
            <p class="blog-text">We don’t actually have any systems to teach people a real step-by-step process.</p>
            <p class="blog-text">This is a BIG problem.</p>
            <p class="blog-text font-bold">The reason why network marketing is not taken seriously is because the
                leadership doesn’t train their people
                as if they are operating a real business.</p>
            <p class="blog-text">For instance, you go to a marketing conference for chiropractors and there’s going to
                be a lot of talk about
                the nuts-and-bolts of marketing and sales — there’s actual skill-based training that’s being given.</p>
            <p class="blog-text">But in network marketing, you’re just told…</p>
            <p class="blog-quote">“Bring more people. It’s going to be easy. It’s not selling, it’s sharing.”</p>
            <h1 class="blog-header">The dumbing down of the profession is a real problem</h1>
            <img class="blog-image-inline"
                 src="http://elitemarketingpro.com/blog/wp-content/uploads/2017/06/86521_BlogBanner2_060217_02.png"/>
            <p class="blog-text">It begins with all the hype and it needs to change, because it gives this industry a
                bad name.</p>
            <p class="blog-text">The bottom line is people are not getting real business training, even though you tell
                them “it’s a real
                business.”</p>
            <p class="blog-text">I first went online because somebody was willing to teach me a process-oriented roadmap
                towards achieving my
                goals, and that’s exactly what I was able to do.</p>
            <p class="blog-text">Was it easy?</p>
            <p class="blog-text font-bold">Well, I’d say, relative to working 48 hours a week for 40 years…YES, it absolutely
                was.</p>
            <p class="blog-text">But it still took a lot of work.</p>
            <p class="blog-text">It took a couple of years for me to fully master the skillsets that allowed me to build
                online.</p>
            <p class="blog-text">However you build, you can learn skillsets that are far better and more efficient than
                most of the stuff
                that’s out there right now.</p>
            <p class="blog-text">Online or offline, you need the proper skills and training.</p>
            <img class="blog-image-center"
                 src="http://elitemarketingpro.com/blog/wp-content/uploads/2017/06/86520_BlogBanner1_060217_2.png"/>
            <p class="blog-text">Another thing that’s wrong with network marketing is the emphasis on recruitment.</p>
            <p class="blog-text">What’s wrong is essentially “recruitment only” based building.</p>
            <p class="blog-text">Many companies focus solely on recruitment and not enough on generating customers and
                creating retail
                sales.</p>
            <p class="blog-text font-bold">This turns ANY company into a thinly-veiled pyramid scheme, which is going to cause
                problems with the
                FTC.</p>
            <p class="blog-text">But that’s not the only problem.</p>
            <h1 class="blog-header">You need to make retail sales for many reasons…</h1>
            <img class="blog-image-inline"
                 src="http://elitemarketingpro.com/blog/wp-content/uploads/2017/06/87177_BlogBanner2_060617_03.png"/>
            <p class="blog-text">First because you can only recruit so many people, eventually you max out, and there’s
                only so much personal
                consumption that can happen.</p>
            <p class="blog-text">Sure you might be able to make a lot of money initially, but eventually you’ll burn
                out.</p>
            <p class="blog-text">That’s why you see big earners who join a company, get into momentum, hit it out of the
                park, and immediately
                make a ton of money.</p>
            <p class="blog-text">But then, all of a sudden, their numbers start dipping.</p>
            <p class="blog-text font-bold">And what happens after their income starts to fall?</p>
            <p class="blog-text">They switch to another company, go into momentum again, and make more money solely
                through recruiting.</p>
            <p class="blog-text">They don’t make retail sales.</p>
            <p class="blog-text">Their volume is mostly the personal consumption of those they’ve recruited.</p>
            <p class="blog-text">So eventually their income goes back down and they switch companies again.</p>
            <p class="blog-text">And the cycle repeats.</p>
            <h1 class="blog-header">This is what recruitment with no retail sales looks like</h1>
            <img class="blog-image-inline"
                 src="http://elitemarketingpro.com/blog/wp-content/uploads/2017/06/86521_BlogBanner2_060217_01.png"/>
            <p class="blog-text">And the people that get screwed in these recruitment-based models are the regular
                distributors.</p>
            <p class="blog-text">The leaders (and you probably know their names) are always going to thrive as they move
                from one company to
                the next.</p>
            <p class="blog-text">There’s a top earner right now—and I can’t say his name—but I had a great conversation
                with him about a week
                and half ago.</p>
            <p class="blog-text font-bold">He’s secretly building in a very traditional network marketing company.</p>
            <p class="blog-text">He’s been involved in quite a few companies over the years, and now he’s building with
                an old, very
                well-known company.</p>
            <p class="blog-text">He decided…</p>
            <p class="blog-quote">“You know I’m done hopping around different companies, I’m going to build this one
                company.”</p>
            <p class="blog-text">When he told me which company he’s building with I was like…</p>
            <p class="blog-quote">“Wait, what!?”</p>
            <h1 class="blog-header">And then he showed me the numbers…</h1>
            <a href="http://www.brankoconnect.com/site/signup">
                <img class="blog-image-inline"
                 src="http://elitemarketingpro.com/blog/wp-content/uploads/2017/02/30899_Banners_091316-02-1.jpg"/>
            </a>
            <p class="blog-text">This particular company is extremely heavy on retail.</p>
            <p class="blog-text">What happens is they are so focused on retail that people with relatively small
                organizations are making a
                massive amount of income, and the income is stable.</p>
            <p class="blog-text">He also showed me some of the stats regarding reordering.</p>
            <p class="blog-text">They have thousands of customers who are reordering—some for over 200 months in a
                row!</p>
            <p class="blog-text">When he showed me that I was like…</p>
            <p class="blog-quote">“Okay I totally get why you joined this company.”</p>
            <p class="blog-text">They have a really solid system for creating retail customers that create massive
                incomes, even if people
                aren’t monster recruiters.</p>
            <p class="blog-text">It’s not even an issue with the FTC where you’d think…</p>
            <p class="blog-quote">“Recruitment-based models suck now because the FTC isn’t going to let us have them
                anymore.”</p>
            <p class="blog-text">Set aside the legal issues of recruitment-based businesses…</p>
            <p class="blog-text font-bold">Retail is legitimately a better business model, in my opinion.</p>
            <img class="blog-image-center"
                 src="http://elitemarketingpro.com/blog/wp-content/uploads/2017/06/86520_BlogBanner1_060217_3.png"/>
            <p class="blog-text">People misrepresent network marketing when they say it’s a business.</p>
            <p class="blog-text font-bold">It’s NOT a business—it’s a profession.</p>
            <p class="blog-text">Yup, it’s a profession, just like being a doctor or being a lawyer.</p>
            <p class="blog-text">Simply getting your degree and becoming a doctor or a lawyer doesn’t mean you have a
                “business” in the way
                that Keith Cunningham (and later on Robert Kiyosaki) describe with the concept of the “Cashflow
                Quadrant.”</p>
            <p class="blog-text font-bold">Network marketing is not a business for a few reasons:</p>
            <p class="blog-text">First, because you don’t actually own anything; you don’t own your downline, you don’t
                own your
                distributorship.</p>
            <p class="blog-text">They can be taken away from you at any point if you break a rule, or because you decide
                to build on the
                Internet and your company gets upset.</p>
            <p class="blog-text">Whatever the case, if you have all these restrictions and rules, it’s not a true
                business (and there’s
                nothing wrong with that).</p>
            <p class="blog-text">If you’re going to define it in using the “Cashflow Quadrant,” network marketing is an
                “S” (or self-employed)
                type of deal.</p>
            <h1 class="blog-header">You’re an independent contractor</h1>
            <img class="blog-image-inline"
                 src="http://elitemarketingpro.com/blog/wp-content/uploads/2017/06/86521_BlogBanner2_060217_03.png"/>
            <p class="blog-text">Essentially you’re responsible for producing sales for a company and you get paid
                commissions based on those
                sales.</p>
            <p class="blog-text">And if you build a team you can get commissions based on their performances as
                well.</p>
            <p class="blog-text">This isn’t the same thing as being in business for yourself.</p>
            <p class="blog-text">Now, there’s nothing wrong with that, you just need to recognize this fact.</p>
            <p class="blog-text font-bold">Importantly, you CAN evolve your distributorship into a business.</p>
            <p class="blog-text">And that’s what I teach in <a class="blog-link"  href="http://www.brankoconnect.com/site/signup">my free 10-day online recruiting bootcamp</a>—how
                to take control of your brand so
                that your income is never solely dependent on what happens with your company.</p>
            <p class="blog-text">Now, when I include thinking you’re a business owner in my list of “what’s wrong,” I
                don’t mean this fact is
                something that’s actually wrong with the industry.</p>
            <p class="blog-text">Instead, what’s wrong is how people perceive it, because they’re led to believe they
                are building a real
                business and that’s not true.</p>
            <p class="blog-text font-bold">In reality, they’re developing the skillset for a new profession.</p>
            <p class="blog-text">That’s what you are doing when you’re an “S”:</p>
            <p class="blog-text">You have certain skillsets that are specific to that profession, just like being a
                doctor or a lawyer, who
                have very specific skillsets in those careers, but that doesn’t mean they are business owners.</p>
            <p class="blog-text">Now, if a doctor or lawyer decided to expand their operations, bringing in more doctors
                or lawyers to create
                a firm or practice of some kind, where they had partners and underlings, then that is a real business,
                because the business would keep running on it’s own, even if someone were to walk away.</p>
            <p class="blog-text">And sure, network marketing has some elements of that where you can take a vacation and
                it still keeps
                producing, but if anything big happens, such as the company shuts down, you’re screwed.</p>
            <p class="blog-text font-bold">You have no control over it because it’s not your business.</p>
            <p class="blog-text">…it’s somebody else’s business.</p>
            <p class="blog-text">Now, I do want to clarify something before we move on…</p>
            <h1 class="blog-header">You CAN create a real business by brand loyalty</h1>
            <img class="blog-image-inline"
                 src="http://elitemarketingpro.com/blog/wp-content/uploads/2017/06/87177_BlogBanner2_060617_01.png"/>
            <p class="blog-text">You can convert your distributorship into a real business through building a brand that
                you control.</p>
            <p class="blog-text">Then, your network marketing business is just a part of your overall business
                strategy.</p>
            <p class="blog-text">You can do this through building a presence online, growing an email list where you
                have control over your
                communications with your people, and creating your own products.</p>
            <p class="blog-text">That’s how you create a following and build a brand.</p>
            <p class="blog-text font-bold">That’s a real business.</p>
            <p class="blog-text">And once you develop a following outside of your company, you have leverage, because
                should anything happen
                to your network marketing company, you’ll still have your brand.</p>
            <p class="blog-text">Now your reputation is based on the fact that people know, like, and trust you.</p>
            <p class="blog-text">And your income is based on people’s loyalty to YOU, and not on their loyalty to a
                particular company.</p>
            <p class="blog-text">That’s something you can control and leverage.</p>
            <img class="blog-image-center"
                 src="http://elitemarketingpro.com/blog/wp-content/uploads/2017/06/86520_BlogBanner1_060217_4.png"/>
            <p class="blog-text">The final thing that’s wrong with network marketing is the pettiness.</p>
            <p class="blog-text font-bold">A lot of top earners are spoiled.</p>
            <p class="blog-text">They made their incomes with a very specific skillset, and that became their “truth,”
                which they defend at
                all costs.</p>
            <p class="blog-text">And for whatever reason, they refuse to acknowledge that there are other ways that can
                also be effective.</p>
            <p class="blog-text">This creates a pettiness and insecurity among top leaders.</p>
            <p class="blog-text">Their ego says…</p>
            <p class="blog-quote">“I know the truth. I KNOW how this business is built.”</p>
            <p class="blog-text">Then, when somebody else comes along and does it a different way, it threatens their
                belief system of how
                “right” they are.</p>
            <p class="blog-text">Now, contrast that with, on the other hand, a business owner like me.</p>
            <p class="blog-text">When I hear that somebody’s doing something different—more efficiently, better, using
                different methods,
                etc.—I immediately try to find out how they are doing what they’re doing, so I can incorporate those
                strategies into my business.</p>
            <p class="blog-text">But unfortunately, that’s not how top earners operate in network marketing.</p>
            <p class="blog-text">And I think it has to do with their lack of real business experience.</p>
            <p class="blog-text">So I think there needs to be some humility among a lot of top earners.</p>
            <h1 class="blog-header">The bigger the incomes, the bigger the egos, it seems</h1>
            <img class="blog-image-inline"
                 src="http://elitemarketingpro.com/blog/wp-content/uploads/2017/06/87177_BlogBanner2_060617_02.png"/>
            <p class="blog-text">This pettiness needs to go, because the fact of the matter is, we have lot of diverse
                ways of building this
                business today.</p>
            <p class="blog-text">A couple of days ago I spoke to a woman who was a top earner in her company and she
                built her organization by
                hiring bloggers to write about her business.</p>
            <p class="blog-text">In fact, she consistently hires bloggers to do reviews about her products and her
                company, and this is her
                strategy.</p>
            <p class="blog-text">Every single week she has a new blogger writing about her business and my response
                is…</p>
            <p class="blog-quote">“I’ve never heard of that. It’s weird!”</p>
            <p class="blog-text">Point being that we have an amazing industry and people have diverse ways of building,
                and that should be
                embraced, not shunned.</p>
            <p class="blog-text">Unfortunately, I think this belief in particular is probably the one that gets me in
                trouble most of the
                time.</p>
            <p class="blog-text">It creates riffs with other top earners, who maybe like me personally, but don’t want
                to associate with what
                we do online.</p>
            <p class="blog-text">It either threatens their truth (or their company’s perception of the truth) and
                therefore they don’t want to
                rock the boat.</p>
            <p class="blog-text">So there you have it..</p>
            <p class="blog-text font-bold">That’s what’s wrong with network marketing.</p>
            <p class="blog-text">I would love to see these things to be corrected and for the mindsets of our leadership
                to change.</p>
            <p class="blog-text">We’ll see.</p>
            <p class="blog-text">I don’t suggest you hold your breath.</p>
            <p class="blog-text">But, make no mistake, there’s a lot to be thankful for…</p>
            <h1 class="blog-header">What’s right with network marketing</h1>
            <p class="blog-text">There’s a lot right with network marketing and my criticism is in no way an attempt to
                “prosecute” the
                industry.</p>
            <p class="blog-text">Bottom line—there is NO other industry, there’s no other profession, that allows you to
                start an “S”-type
                business and eventually move it to a “B”-type business, at such a low cost.</p>
            <img class="blog-image-center"
                 src="http://elitemarketingpro.com/blog/wp-content/uploads/2017/06/87175_BlogBanner1_060617_1.png"/>
            <p class="blog-text">There’s nothing else that compares.</p>
            <p class="blog-text">So the #1 thing that’s right is the ability to basically start in this industry and
                have unlimited income
                potential for such a low startup cost.</p>
            <p class="blog-text">And on that note, I’d like to address something…</p>
            <p class="blog-text">Some people complain that they “spent a lot of money” in network marketing and it
                “didn’t work” for them.</p>
            <p class="blog-text">Well, people can pull on my heart strings about a lot of things…but working the
                business for a couple of
                years and investing $10,000/$20,000 before giving up, and then complaining about it…isn’t one of
                them!</p>
            <p class="blog-text font-bold">This issue has more to do with hype and unrealistic expectations than anything
                else.</p>
            <p class="blog-text">Because in ANY other business—even if I started a practice as a lawyer—I’m going to
                need an initial
                investment to start, and if it doesn’t go well, then I will likely be out tens, if not hundreds of
                thousands
                of dollars.</p>
            <p class="blog-text">I didn’t talk about “risk” in terms of what’s wrong with the industry, because there’s
                so little risk
                compared to any other opportunity.</p>
            <p class="blog-text">So, again, what’s right is the low cost of opportunity and the high income
                potential.</p>
            <img class="blog-image-center"
                 src="http://elitemarketingpro.com/blog/wp-content/uploads/2017/06/87175_BlogBanner1_060617_2.png"/>
            <p class="blog-text">What’s also right is the ingrained culture of personal development.</p>
            <p class="blog-text">It’s funny because there’s so little skills-based training.</p>
            <p class="blog-text">And I think the industry tries to make up for it by being insanely good at helping
                people with personal
                development through helping them overcome their own personal demons so they can persevere and eventually
                figure this industry out.</p>
            <p class="blog-text">I’ve gotten so much in “free”—at least they didn’t charge me extra for it—amazing
                personal development that I
                cannot say enough.</p>
            <p class="blog-text">It’s one of the reasons I persevered so long in this industry (to the point of almost
                going into bankruptcy)
                was that I was willing to persevere due to the personal development training I received.</p>
            <p class="blog-text">So personal development is definitely what’s right in this industry.</p>
            <p class="blog-text">No other industry compares.</p>
            <p class="blog-text">Finally, the beautiful thing is that even with all its warts, there’s nothing that can
                duplicate the
                potential of the success inherent in the opportunity of network marketing.</p>
            <p class="blog-text">The truth is…</p>
            <img class="blog-image-center"
                 src="http://elitemarketingpro.com/blog/wp-content/uploads/2017/06/87175_BlogBanner1_060617_3.png"/>
            <p class="blog-text">Network marketing saves people from mediocrity.</p>
            <p class="blog-text">What I truly love about network marketing is that it saved me from mediocrity.</p>
            <p class="blog-text">Given my background as an MIT graduate, the world was my oyster, professionally.</p>
            <p class="blog-text">As far as people were concerned, going into the aerospace industry with top secret
                clearances and working on
                what would seem like pretty cool programs, seemed great.</p>
            <p class="blog-text font-bold">But I didn’t enjoy what I was doing.</p>
            <p class="blog-text">And I felt like I was just selling myself short if I just settled working at a job that
                I didn’t like.</p>
            <p class="blog-text">No matter how much I was being paid, for me that was mediocrity.</p>
            <p class="blog-text">Not being happy and fulfilled at what I was doing was taking it’s toll on me
                emotionally.</p>
            <p class="blog-text">The only option that anyone offered me as an alternative, back in 2005, was network
                marketing.</p>
            <p class="blog-text">This industry saved me from mediocrity and it’s saving people all over the world from
                mediocrity too.</p>
            <p class="blog-text">Even if you ultimately don’t end up building a big network marketing business…</p>
            <h1 class="blog-header">It’s a gateway into entrepreneurship…</h1>
            <p class="blog-text">It’s a gateway towards thinking differently…</p>
            <p class="blog-text">It’s a gateway to personal development…</p>
            <p class="blog-text">And it’s a gateway towards other opportunities that might otherwise not exist.</p>
            <p class="blog-text">Saving people from mediocrity I think is the biggest reason why we should all support
                the profession, while, at the same time, being open and critical about the things that are wrong and can
                be improved.</p>
            <p class="blog-text">That’s not unreasonable, right?</p>
            <p class="blog-text font-bold">So, to recap, what’s WRONG with network marketing…</p>
            <ul class="blog-text">
                <li>The hype and lack of skill-based training (which creates false expectations and makes it seem too
                    easy)
                </li>
                <li>Recruitment-only models, which are not sustainable</li>
                <li>Believing that network marketing is a business and not a profession</li>
                <li>The pettiness, egos, and insecurities of a lot of top earners because they don’t operate like a real
                    business owner
                </li>
            </ul>
            <p class="blog-text font-bold">And again, here’s what’s RIGHT with network marketing…</p>
            <ul class="blog-text">
                <li>Low costs and high income potential</li>
                <li>Ingrained personal development</li>
                <li>Saving people from mediocrity</li>
            </ul>
            <p class="blog-text">We’ve got to be able to look at what’s good and bad, the wrong and the right, and, most
                importantly, what’s going to work and not work for you and your business.</p>
